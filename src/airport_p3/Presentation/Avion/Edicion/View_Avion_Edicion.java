    private Avion toAvion() {
        try{
        Avion a = new Avion();
        a.setIdAvion(idAvion.getText());
        a.setTipoavion((Tipoavion) TipoAviones.getSelectedItem());
        return a;
        }
        catch(NumberFormatException x){
            this.interruption("Datos Erroneos.");
        }
        return null;
    }

