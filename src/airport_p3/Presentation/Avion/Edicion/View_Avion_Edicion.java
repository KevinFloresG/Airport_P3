    @Override
    public void update(Observable o, Object arg) {
        renderAvion(model.getAvion());
    }

    public void renderAvion(Avion a) {
        idAvion.setText(a.getIdAvion());
        this.TipoAviones.setModel(new DefaultComboBoxModel<>(model.getTipoAviones().toArray(new Tipoavion[0])));
        this.TipoAviones.setSelectedItem(a.getTipoavion());
    }

